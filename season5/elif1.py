
a = int(input("Thang sinh cua ban: "))
if a <= 0 or a >= 13:
    print("Than sinh cua ban khong hop le.")
if 0 < a < 4:
    print("Ban sinh vao mua: Xuan")
elif 0 < a < 7:
    print("Ban sinh vao mua: Ha")
elif 0 < a < 10:
    print("Ban sinh vao mua: Thu")
elif 0 < a < 13:
    print("Ban sinh vao mua: Dong")

